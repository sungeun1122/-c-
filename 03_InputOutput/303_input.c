#include <stdio.h>
#pragma warning(disable:4996)

/*

*/

int main()
{
	float height, weight;
	printf("키를 입력하세요(cm) : ");  
	scanf("%f", &height); // %f, 키보드로부터 실수 입력받아서  float 변수에 대입
	printf("체중을 입력하세요(kg) : ");
	scanf("%f", &weight);

	printf("키: %.1fcm 체중: %.1fkg\n", height, weight);

	double d1;
	printf("실수를 입력하세요 : ");
	//scanf("%f", &d1);  // double 타입 입력받을때는 %lf 사용
	scanf("%lf", &d1);
	printf("입력하신 실수는 %.12f 입니다\n", d1);


	getchar();
	printf("\nENTER 를 누르면 종료합니다\n");
	getchar();
	return 0;
}